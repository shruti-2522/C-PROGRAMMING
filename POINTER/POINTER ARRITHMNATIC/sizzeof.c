//SIZEOF OPERATOR:

#include<stdio.h>
int main()
{
    int a;
    float b;double c;
    printf("\n SIZE OF INTEGER DATATYPE IS=%d",sizeof(int));
    printf("\n SIZE OF INTEGER VALUE IS=%d",sizeof(a));
    printf("\n SIZE OF FLOAT VALUE IS=%d",sizeof(b));
    printf("\n SIZE OF DOUBBLE VALUE=%d",sizeof(c));
}

/*OUTPUT:
 SIZE OF INTEGER DATATYPE IS=4
 SIZE OF INTEGER VALUE IS=4
 SIZE OF FLOAT VALUE IS=4
 SIZE OF DOUBBLE VALUE=8*/